graphe_NA<-function(result){
	l=0
	position<-numeric(length(result))
	for (i in 1:length(result)){
		for (j in 1:100){
			for (k in 1:60){
				if (result[i] == ((60*(j-1))+k)){
					l=l+1
					position[l]=k
				}
			}
		}
	}
	return(position)
}

